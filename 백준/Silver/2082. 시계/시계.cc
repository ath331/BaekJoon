#include<stdio.h>
char data[4][15], f[][16] = { "####.##.##.####","..#..#..#..#..#","###..#####..###","###..####..####","#.##.####..#..#","####..###..####","####..####.####","###..#..#..#..#","####.#####.####","####.####..####" };
int main()
{
    int i, j, k;
    for (i = 0; i<5; i++)
    {
        for (j = 0; j<12; j++)
        {
            scanf("\n%c", &data[j / 3][i * 3 + j % 3]);
        }
    }
    for (i = 0; i<4; i++)
    {
        for (j = 0; j <= 9; j++)
        {
            for (k = 0; k<15; k++)
            {
                if (data[i][k] == '#'&&f[j][k] == '.')
                {
                    break;
                }
            }
            if (k == 15) { printf("%d", j); break; }
        }
        if (i == 1) printf(":");
    }
    return 0;
}